
public abstract class Ricercatori {
	String id;
	Float basicSalary;
	
	
	public Ricercatori(String id) {		
		this.id = id;
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Float getBasicSalary() {
		return basicSalary;
	}
	public void setBasicSalary(Float basicSalary) {
		this.basicSalary = basicSalary;
	}


		
	
}
